    # Task 1: Value Swapping

# Given values
a = 33
b = 21

# Swapping Values With Assignment Operators
a, b = b, a

# Print Swapped Values
print(f"After swapping, the value of a is: {a}")
print(f"After swapping, the value of b is: {b}")

# Results of Swapped Values
if a == 21 and b == 33:
    print("Values have been successfully swapped.")
else:
    print("Values have not been swapped.")




